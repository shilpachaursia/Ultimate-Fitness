package com.fitnessapplication.ultimatefitness.male;

public class ModelMen {
    private int image;
    private String exerciseName, exerciseStart;


    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getExerciseName() {
        return exerciseName;
    }

    public void setExerciseName(String exerciseName) {
        this.exerciseName = exerciseName;
    }

    public String getExerciseStart() {
        return exerciseStart;
    }

    public void setExerciseStart(String exerciseStart) {
        this.exerciseStart = exerciseStart;
    }
}
